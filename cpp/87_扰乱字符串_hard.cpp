class Solution {
private:
    string s1, s2;
    int memo[30][30][31];
public:
    bool checkIfSimilar(int i1, int i2, int length) {
        unordered_map<int, int> freq;
        for (int i=i1; i<i1+length; i++) ++freq[s1[i]];
        for (int i=i2; i<i2+length; i++) --freq[s2[i]];
        if (any_of(freq.begin(), freq.end(), [](const auto &entry) {
            return entry.second != 0;
        })) return false;
        return true;
    }
    bool dfs(int i1, int i2, int length) {
        if (memo[i1][i2][length]) return memo[i1][i2][length] == 1;
        if (s1.substr(i1, length) == s2.substr(i2, length)) {
            memo[i1][i2][length] = 1;
            return true;
        }
        if (!checkIfSimilar(i1, i2, length)) {
            memo[i1][i2][length] = -1;
            return false;
        }
        for (int i=1; i<length; i++) {
            if (dfs(i1, i2, i) && dfs(i1+i, i2+i, length-i)) {
                memo[i1][i2][length] = 1;
                return true;
            }
            if (dfs(i1, i2+length-i, i) && dfs(i1+i, i2, length-i)) {
                memo[i1][i2][length] = 1;
                return true;
            }
        }
        memo[i1][i2][length] = -1;
        return false;
    }
    bool isScramble(string s1, string s2) {
        int length = s1.length();
        memset(memo, 0, sizeof(memo));
        this->s1 = s1;
        this->s2 = s2;
        return dfs(0, 0, length);
    }
};